void	*ft_memcpy(void *dest, void *src, int n)
{
	char *s;
	char *d;

	s = (char *)src;
	d = (char *)dest;
	while (n > 0)
	{
		d[n - 1] = s[n - 1];
		n--;
	}
	return (dest);
}
