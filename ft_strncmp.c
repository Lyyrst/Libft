int	ft_strncmp(char *s1, char *s2, int n)
{
	int	i;

	i = -1;
	while (n > 0 && (s1[++i] || s2[i]))
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		n--;
	}
	return (0);
}
