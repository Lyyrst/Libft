int	ft_strlen(char *str)
{
	int	count;

	count = 0;
	while (str[count])
		count++;
	return (count);
}

int	ft_strlcat(char *dest, char *src, int size)
{
	int	i;
	int	ldest;
	int	lsrc;

	i = 0;
	ldest = ft_strlen(dest);
	lsrc = ft_strlen(src);
	if (size - 1 < ldest)
		return (size + lsrc);
	while (src[i])
	{
		if (size > 0)
			dest[ldest + i] = src[i];
		i++;
	}
	dest[ldest + i] = 0;
	return (ldest + lsrc);
}
